const int kMod = 1e9 + 7;
struct ModInt {
    long long n;
    ModInt(long long n = 0) : n(n % kMod) {}
    ModInt operator+(const ModInt& oth) { return n + oth.n; }
    ModInt operator-(const ModInt& oth) { return n - oth.n; }
    ModInt operator*(const ModInt& oth) { return n * oth.n; }
    long long get() { return n < 0 ? n + kMod : n; }
};
ModInt lgpow(ModInt b, int e = -1) {
    if (b.get() == 0) return 0;
    ModInt r;
    e = e % (kMod - 1);
    if (e < 0) e += kMod - 1;
    for (r = 1; e; e >>= 1, b = b * b)
        if (e & 1) r = r * b;
    return r;
}
void solve() {
    ll n, k;
    cin >> n >> k;
    if (n == 0) {
        ModInt ans = k * (k - 1);
        cout << ans.get() << '\n';
        return;
    }
    ModInt t = n + k / 2;
    ModInt ans = t * (t - 1);
    ModInt res = n + 1;
    if (k % 2) res = t * 2 + 1 - n;
    ans = ans + res - 1;
    cout << ans.get() << '\n';
}
int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t = 1;
    cin >> t;
    while (t--) solve();
}